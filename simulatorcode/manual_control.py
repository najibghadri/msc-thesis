            array = np.reshape(array, (image.height, image.width, 4))
            array = array[:, :, :3]
            array = array[:, :, ::-1]
            Image.fromarray(array, 'RGB').save(('%s/out/%d%s.jpg' % (self.path, image.frame, label)), 'JPEG', quality=95)
