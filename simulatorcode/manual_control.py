def rotateVector(rotation, vector):
    return R.from_euler('zyx', [rotation.yaw, rotation.pitch, rotation.roll], degrees=True).apply(np.array([vector.x, vector.y, vector.z]), inverse=True)

def rotationDistance(rot1, rot2):
    return edict({
        "pitch": rot1.pitch - rot2.pitch,
        "yaw": rot1.yaw - rot2.yaw,
        "roll": rot1.roll - rot2.roll ,
    })

def rotationToObj(rot):
    return edict({
        "pitch": rot.pitch,
        "yaw": rot.yaw,
        "roll": rot.roll,
    })

def vectorToObj(vector):
    return edict({
        "x": vector.x,
        "y": vector.y,
        "z": vector.z,
    })

def listVectorToObj(vector):
    return edict({
        "x": vector[0],
        "y": vector[1],
        "z": vector[2],
    })

